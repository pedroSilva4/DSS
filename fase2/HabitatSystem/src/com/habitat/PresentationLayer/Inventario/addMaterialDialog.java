    private void add_bttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_bttActionPerformed
        // TODO add your handling code here:
        String desc = this.descricao.getText();
        String unids  =this.unid.getText();
        int qt;
        try{
            qt =  Integer.parseInt(this.qtt.getText());
        }catch(NumberFormatException ex){
            qt = -1;
            new ErrorWindow("quantidade","Quantidade não é um numero" , "error", new JFrame()).wshow();
        }
        if(qt!=-1){
                try {
                    bus.addMaterial(desc,unids,qt);
                } catch (SQLException ex) {

                     new ErrorWindow("Erro",ex.getMessage(), "error", new JFrame()).wshow();
                }
        }
        ((JFrame)this.getParent()).dispose();
    }//GEN-LAST:event_add_bttActionPerformed

