    private KeyListener kl;
    
    private void initListener(){
        this.kl = new KeyListener() {

                    @Override
                    public void keyTyped(KeyEvent e) {
                         if(!unid.getText().isEmpty() && !qtt.getText().isEmpty() && !descricao.getText().isEmpty())
                                 add_btt.setEnabled(true);
                         else add_btt.setEnabled(false);
                    }

                    @Override
                    public void keyPressed(KeyEvent e) {
                        if(!unid.getText().isEmpty() && !qtt.getText().isEmpty() && !descricao.getText().isEmpty())
                                 add_btt.setEnabled(true);
                        else add_btt.setEnabled(false);
                    }

                    @Override
                    public void keyReleased(KeyEvent e) {
                         if(!unid.getText().isEmpty() && !qtt.getText().isEmpty() && !descricao.getText().isEmpty())
                                 add_btt.setEnabled(true);
                         else add_btt.setEnabled(false);
                    }
        };
        
        this.descricao.addKeyListener(kl);
        this.qtt.addKeyListener(kl);
        this.unid.addKeyListener(kl);
    }
